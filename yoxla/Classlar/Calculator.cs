using System;
namespace Classlar
{
    public class Calculator
    {
        public int Cem(int a, int b)
        {
            int c = a + b;
            return c;
        }

        public int Ferq(int a, int b)
        {
            int c = a - b;
            return c;
        }

        public int Hasil(int a, int b)
        {
            int c = a * b;
            return c;
        }

        public int Qismet(int a, int b)
        {
            int c = a / b;
            return c;
        }
    }
}
